const LOCALES = { en: EN, fr: FR };

export function createT(localeCode) {
  const lang = normalizeLang(localeCode);
  const primary = LOCALES[lang];
  const fallback = LOCALES.en;
  return function t(key, vars) {
    let s = get(primary, key);
    if (s == null) s = get(fallback, key);
    if (s == null) return key;
    if (vars && typeof s === 'string') {
      return s.replace(/\{\{(\w+)\}\}/g, (_, n) => (vars[n] != null ? String(vars[n]) : ''));
    }
    return s;
  };
}
